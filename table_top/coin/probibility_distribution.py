def create_scenarios(distribution: dict[int, int], num_of_scenarios: int) -> tuple[int]:
    start = 0
    for wins, percentage in distribution.items():
        end = start + round((percentage/100)*num_of_scenarios)
        for _ in range(start, end):
            yield wins

        start = end


